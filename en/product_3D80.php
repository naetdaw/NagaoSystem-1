                        <u style="font-size: 1.3em; color: green; text-align: right;">Tilted Rotation Planetary Ball Mill
Example of pulverization of Planet M2-3F②
Patent No. 4937502 Planetary ball mill
July 26, 2005</u>
                           <br>
                           <br>
①Dry process pulverization without solidification <br>
②Low thermogenesis with spherical motion  <br>
③Uniform pulverization, mixing and dispersion <br>
④Non-criticality based on high-speed motion <br>
⑤Uniform mixing without propeller/blade <br>
⑥Utilizing friction force and achieved pulverization with maintaining particle form
 <br>
  <br>
【Merit】 <br>
①Thermogenesis problem in 2D motion pulverization <br>
【Solution】 <br>
・Utilizing the entire inner area of the container promotes the dispersion of  <br>frictional heat and its generation. Effective with organic and inorganic substances.
 <br> 
    <br>
②Uneven particle size in 2D motion pulverization <br>
【Solution】 <br>
・Batched pulverization utilizing friction force of high-speed 3D motion. Rounded  <br>particle form and uniform particle size. 
<br>
     <br>
③Solidification in 2D motion dry pulverization <br>
【Solution】 <br>
・Achieving no solidification by high-speed 3D motion dry pulverization.
 <br>
     <br>
④Uneven 2D motion mixing (because of only one axis) <br>
【Solution】 <br>
・Without propeller/blade. High-speed 3D motion makes it possible to use entire inner area of container efficiently. Low thermogenesis and uniform mixing can be achieved.
